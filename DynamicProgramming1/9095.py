tc = int(input())


# 1 -> 1
# 2 -> 1+1, 2
# 3 -> 1+2, 1+1+1, 2+1, 3
# 4 -> 1+3, 1+1+2. 2+2, 1+2+1, 1+1+1+1, 2+1+1, 3+1
# 5 -> 1+1+3, 2+3, 1+2+2, 1+1+1+2, 2+1+2, 3+2, 1+3+1, 1+1+2+1, 2+2+1, 1+2+1+1, 1+1+1+1+1, 2+1+1+1, 3+1+1

def solution(n):
    dp = [0, 1, 2, 4]+([0]*(n-3))

    for i in range(4, n+1):
        dp[i] = dp[i-1]+dp[i-2]+dp[i-3]
    return dp[n]


for _ in range(tc):
    print(solution(int(input())))
